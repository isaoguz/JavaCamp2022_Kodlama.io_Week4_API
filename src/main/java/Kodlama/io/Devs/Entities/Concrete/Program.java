package Kodlama.io.Devs.Entities.Concrete;

public class Program {
	int id;
	String name;
	
	
	
	public Program() {
		super();
	}



	public Program(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}



	public int getId() {
		return id;
	}



	public void setId(int id) {
		this.id = id;
	}



	public String getName() {
		return name;
	}



	public void setName(String name) {
		this.name = name;
	}
	
	
	
	
	
	
}
